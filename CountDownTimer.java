    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {                                         

        btnStart.setEnabled(false);
        btnMinAdd10.setEnabled(false);
        btnMinAdd15.setEnabled(false);
        btnMinAdd30.setEnabled(false);
        cbxHr.setEnabled(false);
        cbxMin.setEnabled(false);
        cbxSec.setEnabled(false);
        
        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                btnStart.setEnabled(false);
                
                if (isStop) {
                    hr = hr1;
                    min = min1;
                    sec = sec1;
                    isStop = false;
                }
                if (sec < 1) {
                    sec = 60;
                    min--;
                }
                if (min < 0) {
                    min = 59;
                    hr--;
                }
                if (hr < 0) {
                    JOptionPane.showMessageDialog(rootPane, "Time Out!");
                    hr = 0;
                    min = 0;
                    sec = 0;
                    timer.stop();
                    btnStart.setEnabled(true);
                } else {
                    sec--;
                }
                if (sec < 10) {
                    lblSec.setText("0" + sec);
                } else {
                    lblSec.setText("" + sec);
                }

                if (min < 10) {
                    lblMin.setText("0" + min);
                } else {
                    lblMin.setText("" + min);
                }

                if (hr < 10) {
                    lblHr.setText("0" + hr);
                } else {
                    lblHr.setText("" + hr);
                }
            }
        });
        timer.start();
    }                                        

